        <p>Sprouts Salad</p>


        <img src="h1.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>1 cup mixed sprouts (mung, moth, chana, etc.)</li>
            <li>1 small onion, finely chopped</li>
            <li>1 small tomato, finely chopped</li>
            <li>1 small cucumber, finely chopped</li>
            <li>1 small carrot, grated</li>
            <li>1/4 cup fresh coriander leaves, chopped</li>
            <li>1/2 cup pomegranate fruit, fresh kernels</li>
            <li>Juice of 1 lemon</li>
            <li>1/2 tsp chaat masala (optional)</li>
            <li>Salt to taste</li>
        </ul>



        <h3>Instructions: </h3>
        <ol>
            <li>Wash and soak the mixed sprouts in enough water for at least 6-8 hours or overnight.
            </li>
            <li>Drain the water and rinse the sprouts under running water.
            </li>
            <li>Transfer the sprouts to a clean muslin cloth and tie a knot. Keep it in a warm place for 6-8 hours to
                allow the sprouts to grow.
            </li>
            <li>Once the sprouts are ready, transfer them to a large mixing bowl.
            </li>
            <li>Add finely chopped onion, tomato, cucumber, pomegranate kernels and grated carrot to the bowl.
            </li>
            <li>Add chopped coriander leaves, lemon juice, chaat masala, and salt to taste.
            </li>
            <li>Mix all the ingredients well and serve.
            </li>
        </ol>

