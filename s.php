           <h3>dosa</h3>
            <h4>Ingredients:</h4>

<li>1 cup rice</li>
<li>1/2 cup urad dal (split black gram)</li>
<li>1/4 tsp fenugreek seeds</li>
<li>Water, as needed</li>
<li>Salt, to taste</li>
<li>Oil, for cooking</li>
<h4>Method</h4>

<li>1]Wash the rice, urad dal, and fenugreek seeds separately and soak them in water for at least 4 hours.</li>

<li>2]After soaking, drain the water from the rice and dal separately.</li>

<li>3]Grind the urad dal and fenugreek seeds in a blender or wet grinder to a smooth and fluffy batter. Add water as needed, but don't make it too thin.</li>

<li>4]Transfer the urad dal batter to a large bowl.</li>

<li>5]Next, grind the rice to a fine paste, adding water as needed. Once it is done, add it to the urad dal batter, add salt, and mix well.</li>

<li>6]Cover the bowl and let the batter ferment in a warm place for at least 8 hours or overnight.</li>

<li>7]Once the batter is fermented, give it a good stir.Heat a non-stick or cast-iron skillet on medium heat.</li>
<li>8]Spread a ladleful of the batter onto the skillet in a circular motion to form a thin layer. Add a few drops of oil around the edges.Cook the dosa for 1-2 minutes or until the bottom turns golden brown.</li>

<li>9]Flip the dosa over and cook the other side for another minute or until it is cooked through.Serve hot with chutney, sambar, or your favorite side dish.</li>
<h3>mendu vada</h3>
<h4>integreds</h4>

    <li>1 cup urad dal (split black gram)</li>
    <li>1 tbsp rice flour</li>
    <li>1/4 tsp asafoetida (hing)</li>
    <li>2-3 green chilies, finely chopped</li>
    <li> inch ginger, grated</li>
    <li>1 tbsp chopped curry leaves</li>
    <li>Salt, to taste</li>
    <li>Oil, for frying</li>
    <h4>Instructions:</h4>
    
    <li>1]Wash the urad dal and soak it in water for at least 4 hours.</li>
    
    <li>2]After soaking, drain the water and grind the urad dal in a blender or wet grinder to a smooth and fluffy batter. Add water as needed, but don't make it too thin.</li>
    
    <li>3]Transfer the batter to a large bowl and add rice flour, asafoetida, chopped green chilies, grated ginger, chopped curry leaves, and salt. Mix well to combine.Heat oil in a deep frying pan on medium-high heat.</li>
    <li>4]Wet your hands with water, take a small portion of the batter and flatten it into a circular shape with a hole in the middle. You can also use a wet spoon to shape the batter.</li>
    
    <li>5]Carefully slide the vada into the hot oil and fry until it turns golden brown on both sides. Remove the vada from the oil using a slotted spoon and drain excess oil on a paper towel.</li>
    <li>6] Repeat the process with the remaining batter.Serve the medu vada hot with coconut chutney and sambar.</li>
    <h3>idli</h3>
    <h4>ingredients</h4>
  

<li>1 cup parboiled rice</li>
<li>1 cup whole or split urad dal</li>
<li>1 teaspoon fenugreek seeds</li>
<li>Salt to taste</li>
<li>Water as needed</li>
<li>Oil for greasing the idli plates</li>
<h4>Method</h4>
<li>1]Rinse the rice and urad dal separately a few times in water.</li>
<li>2]Soak the rice, urad dal, and fenugreek seeds in separate bowls with enough water to cover them for at least 4-6 hours or overnight.Drain the water from the soaked urad dal and grind it in a blender or wet grinder with enough water to make a smooth and fluffy batter.</li>
<li>3]Remove the urad dal batter into a large bowl Grind the soaked rice with enough water to make a smooth batter. The batter should be slightly grainy in texture.</li>
<li>4]Add the rice batter to the bowl with the urad dal batter and mix well.Add salt to taste and mix well again.Cover the bowl with a lid or plastic wrap and let it ferment in a warm place for at least 6-8 hours or overnight.</li>
<li>5]After fermentation, the batter will have risen and become slightly sour in taste.Mix the batter well and add a little water if needed to get a pouring consistency.</li>
<li>6]Grease the idli plates with oil and pour the batter into each cavity, filling it about 3/4th of the way.</li>
<li>7]Place the idli plates in a steamer and steam the idlis for 10-12 minutes on high heat until they are cooked through and fluffy.</li>
<li>8]Once cooked, remove the idli plates from the steamer and let them cool for a minute.</li>

