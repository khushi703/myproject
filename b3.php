        <p>Pina Colada</p>


        <img src="b3.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>2 oz. (60 ml) white rum</li>
            <li>3 oz. (90 ml) pineapple juice</li>
            <li>2 oz. (60 ml) cream of coconut</li>
            <li>1 cup (240 ml) ice</li>
            <li>Pineapple wedge and maraschino cherry (optional, for garnish)</li>


        </ul>



        <h3>Instructions:</h3>
        <ol>
            <li>Combine the white rum, pineapple juice, cream of coconut, and ice in a blender.
            </li>
            <li>Blend on high until smooth and creamy.
            </li>
            <li>Pour the mixture into a tall glass.
            </li>
            <li>Garnish with a pineapple wedge and a maraschino cherry, if desired.
            </li>
            <li>Serve immediately and enjoy your refreshing Pina Colada!
            </li>
            <li>Note: If you prefer a frozen Pina Colada, use frozen pineapple chunks instead of ice and blend until
                smooth.
            </li>
        </ol>

