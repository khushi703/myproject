        <p>Greek Salad</p>


        <img src="h3.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>2 large tomatoes, diced</li>
            <li>1 English cucumber, diced</li>
            <li>1 red onion, thinly sliced</li>
            <li>1 green bell pepper, diced</li>
            <li>1/2 cup Kalamata olives</li>
            <li>1/2 cup crumbled feta cheese</li>
            <li>2 tablespoons red wine vinegar</li>
            <li>1/4 cup olive oil</li>
            <li>1/2 teaspoon dried oregano</li>
            <li>Salt and pepper, to taste</li>
        </ul>



        <h3>Instructions: </h3>
        <ol>
            <li>In a large mixing bowl, combine the diced tomatoes, diced cucumber, thinly sliced red onion, diced green
                bell pepper, and Kalamata olives.
            </li>
            <li>In a small mixing bowl, whisk together the red wine vinegar, olive oil, dried oregano, salt, and pepper.
            </li>
            <li>Pour the dressing over the salad and toss to combine.
            </li>
            <li>Sprinkle the crumbled feta cheese over the top of the salad.
            </li>
            <li>Serve chilled or at room temperature.
            </li>
        </ol>

