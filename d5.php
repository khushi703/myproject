        <p>Tiramisu</p>


        <img src="dd5.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>6 egg yolks</li>
            <li>3/4 cup granulated sugar</li>
            <li>2/3 cup milk</li>
            <li>1 1/4 cups heavy cream</li>
            <li>1/2 tsp vanilla extract</li>
            <li>1/2 cup strong brewed espresso or coffee, cooled</li>
            <li>3 tbsp rum or marsala wine</li>
            <li>18-20 ladyfingers</li>
            <li>1/4 cup cocoa powder</li>
        </ul>



        <h3>Instructions:</h3>
        <ol>
            <li>In a large bowl, beat the egg yolks and sugar together until pale yellow and thick.
            </li>
            <li>In a saucepan, heat the milk over medium heat until it just comes to a boil.
            </li>
            <li>Gradually whisk the hot milk into the egg yolk mixture until fully combined.
            </li>
            <li>Return the mixture to the saucepan and cook over low heat, stirring constantly, until the mixture
                thickens enough to coat the back of a spoon.
            </li>
            <li>Remove from heat and let cool to room temperature.
            </li>
            <li>In a separate bowl, beat the heavy cream and vanilla extract until soft peaks form.
            </li>
            <li>Fold the whipped cream into the cooled custard mixture until well combined.
            </li>
            <li>In a shallow dish, mix together the cooled espresso or coffee and rum or marsala wine.
            </li>
            <li>Dip each ladyfinger into the espresso mixture and arrange them in a single layer in the bottom of an 8x8
                inch dish.
            </li>
            <li>Spread half of the custard mixture over the ladyfingers.
            </li>
            <li>Repeat the layering process with the remaining ladyfingers and custard mixture.
            </li>
            <li>Cover the dish and chill in the refrigerator for at least 2 hours, or overnight.
            </li>
            <li>Just before serving, sprinkle the cocoa powder evenly over the top of the tiramisu.
            </li>
        </ol>

