        <p>Falafel</p>


        <img src="a5.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>1 cup dried chickpeas (or 2 cans of chickpeas, drained and rinsed)</li>
            <li>1 small onion, chopped</li>
            <li>3 garlic cloves, chopped</li>
            <li>1/4 cup fresh parsley leaves, chopped</li>
            <li>1/4 cup fresh coriander leaves, chopped</li>
            <li>1 tsp ground cumin</li>
            <li>1 tsp ground coriander</li>
            <li>1/2 tsp baking powder</li>
            <li>Salt and pepper to taste</li>
            <li>Vegetable oil for frying</li>


        </ul>






        <h3>Instructions:</h3>
        <ol>
            <li>If using dried chickpeas, soak them in water overnight or for at least 6-8 hours until they double in
                size. Drain and rinse well.</li>
            <li>In a food processor, combine the chickpeas, chopped onion, garlic, parsley, coriander, cumin, coriander,
                baking powder, salt, and pepper. Pulse until the mixture is finely ground but not pureed, with some
                texture remaining.</li>
            <li>Transfer the falafel mixture to a bowl and cover it with plastic wrap. Refrigerate for at least 30
                minutes to 1 hour to allow the flavors to meld and the mixture to firm up.</li>
            <li>Heat vegetable oil in a deep frying pan or a deep fryer until it reaches 375Â°F.</li>
            <li>Using a small cookie scoop or a tablespoon, scoop the falafel mixture and shape it into small balls or
                patties, about 1 inch in diameter.</li>
            <li>Carefully drop the falafel balls or patties into the hot oil, a few at a time, and fry until they are
                golden brown and crispy on the outside, about 2-3 minutes.</li>
            <li>Remove the falafel from the oil with a slotted spoon and drain them on a paper towel-lined plate.</li>
            <li>Serve hot with pita bread, tahini sauce, hummus, chopped vegetables, and pickles. </li>
        </ol>


