        <p>Mendu Vada</p>


        <img src="t2.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>1 cup urad dal (split black gram)</li>
            <li>1-2 green chilies, finely chopped</li>
            <li>1 inch ginger, grated</li>
            <li>A handful of curry leaves, finely chopped</li>
            <li>1 tbsp whole black peppercorns</li>
            <li>1 tbsp cumin seeds</li>
            <li>Salt to taste</li>
            <li>Oil for deep frying</li>
        </ul>

        <h3>Instructions: </h3>
        <ol>
            <li>Wash the urad dal in water 2-3 times and soak it in water for 3-4 hours.
            </li>
            <li>Drain the water from the soaked dal and grind it to a smooth paste without adding any water. You can use
                a wet grinder or a food processor for this.
            </li>
            <li>Add chopped green chilies, grated ginger, chopped curry leaves, black peppercorns, cumin seeds and salt
                to the dal paste. Mix well.
            </li>
            <li>Heat oil in a deep frying pan or kadhai over medium flame.
            </li>
            <li>Take a small amount of the dal mixture and shape it into a small ball.
            </li>
            <li>Wet your palms with water and flatten the ball between your palms to form a donut shape with a hole in
                the center. Alternatively, you can also use a greased plastic sheet or banana leaf to shape the vada.
            </li>
            <li>Gently slide the vada into the hot oil and fry until it turns golden brown and crispy on both sides.
            </li>
            <li>Remove the vada from the oil using a slotted spoon and place it on a paper towel to remove any excess
                oil.
            </li>
            <li>Repeat the same process with the remaining dal mixture to make more vadas.
            </li>
            <li>Serve hot with coconut chutney, sambar or tomato chutney.
            </li>
        </ol>
