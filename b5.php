        <p>Classic Indian Lassi</p>


        <img src="b5.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>1 cup plain yogurt</li>
            <li>1/2 cup cold water</li>
            <li>1/2 cup ice cubes</li>
            <li>2-3 tablespoons sugar (to taste)</li>
            <li>1/4 teaspoon ground cardamom (optional)</li>
            <li>Pinch of salt</li>
        </ul>



        <h3>Instructions:</h3>
        <ol>
            <li>In a blender, combine the plain yogurt, cold water, ice cubes, sugar, ground cardamom (if using), and a
                pinch of salt.
            </li>
            <li>Blend the mixture on high speed until it is smooth and frothy.
            </li>
            <li>Taste the lassi and adjust the sweetness to your liking by adding more sugar if needed.
            </li>
            <li>Pour the lassi into glasses and garnish with a sprinkle of ground cardamom (if desired).
            </li>
        </ol>

