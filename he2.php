        <p>Vegetable Khichadi</p>


        <img src="h2.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>1 cup rice</li>
            <li>1/2 cup moong dal (split and skinned green lentils)</li>
            <li>1 small onion, finely chopped</li>
            <li>1 small tomato, finely chopped</li>
            <li>1 small carrot, chopped</li>
            <li>1 small potato, chopped</li>
            <li>1/2 cup green peas</li>
            <li>1/2 tsp cumin seeds</li>
            <li>1 tsp ginger paste</li>
            <li>2-3 garlic cloves, minced</li>
            <li>1/4 tsp turmeric powder</li>
            <li>1/2 tsp red chilli powder (optional)</li>
            <li>Salt to taste</li>
            <li>1 tbsp ghee or oil</li>
            <li>3 cups water</li>
            <li>Coriander leaves, for garnishing</li>

        </ul>



        <h3>Instructions: </h3>
        <ol>
            <li>Wash the rice and moong dal together and soak them in enough water for 30 minutes.
            </li>
            <li>Drain the water and keep the rice and dal aside.
            </li>
            <li>In a pressure cooker, heat ghee or oil over medium heat.
            </li>
            <li>Add cumin seeds and let them splutter.
            </li>
            <li>Add chopped onions, minced garlic, and ginger paste. Saut√© until the onions turn golden brown.
            </li>
            <li>Add chopped tomatoes and cook until they become soft and mushy.
            </li>
            <li>Add chopped vegetables, turmeric powder, red chilli powder, and salt. Mix well.
            </li>
            <li>Add the soaked rice and dal to the pressure cooker and mix well with the masala.
            </li>
            <li>Add 3 cups of water and mix well.
            </li>
            <li>Close the pressure cooker and cook on high heat until the first whistle. Then reduce the heat to low and
                let it cook for 10-15 minutes.
            </li>
            <li>Turn off the heat and let the pressure release naturally.
            </li>
            <li>Open the pressure cooker and give the khichdi a gentle stir.
            </li>
            <li>Garnish with chopped coriander leaves and serve hot with yogurt or pickle.
            </li>
        </ol>

