        <img src="t5.jpg">
        <h3>For the Chicken Marinade:</h3>
        <ul>
            <li>500 g boneless chicken, cut into medium-sized pieces</li>
            <li>1 cup plain yogurt</li>
            <li>2 tbsp ginger garlic paste</li>
            <li>1 tsp red chili powder</li>
            <li>1 tsp turmeric powder</li>
            <li>1 tsp garam masala powder</li>
            <li>1 tsp cumin powder</li>
            <li>Salt to taste</li>
        </ul>
        <h3>For the Biryani:</h3>
        <ul>
            <li>2 cups Basmati rice</li>
            <li>1 onion, thinly sliced</li>
            <li>2-3 green chilies, slit lengthwise</li>
            <li>2-3 bay leaves</li>
            <li>2-3 cardamom pods</li>
            <li>2-3 cinnamon sticks</li>
            <li>4-5 cloves</li>
            <li>1 tsp cumin seeds</li>
            <li>1 tsp coriander seeds</li>
            <li>1 tsp fennel seeds</li>
            <li>2 tbsp ghee</li>
            <li>2 tbsp oil</li>
            <li>Salt to taste</li>
            <li>2 cups water</li>
            <li>1/2 cup chopped cilantro</li>
            <li>1/2 cup fried onions</li>
        </ul>

        <h3>Instructions:

        </h3>
        <ol>
            <li>Wash the rice in water and soak it in enough water for 30 minutes.
            </li>
            <li>To make the chicken marinade, mix together yogurt, ginger garlic paste, red chili powder, turmeric
                powder, garam masala powder, cumin powder, and salt in a bowl.
            </li>
            <li>Add chicken pieces to the marinade and mix well. Cover and refrigerate for at least 2 hours or
                overnight.
            </li>
            <li>Heat oil and ghee in a heavy-bottomed pan or a Dutch oven over medium-high heat.
            </li>
            <li>Add sliced onions and sauté until golden brown.
            </li>
            <li>Add green chilies, bay leaves, cardamom pods, cinnamon sticks, cloves, cumin seeds, coriander seeds, and
                fennel seeds. Sauté for a minute.
            </li>
            <li>Add marinated chicken to the pan and sauté for 5-6 minutes until the chicken is browned on all sides.
            </li>
            <li>Add 2 cups of water and salt to taste. Mix well and bring it to a boil.
            </li>
            <li>Drain the water from the soaked rice and add it to the pan. Mix well and bring it to a boil.
            </li>
            <li>Cover the pan with a tight-fitting lid and cook for 15-20 minutes on low heat until the rice and chicken
                are cooked through.
            </li>
            <li>Turn off the heat and let it rest for 10-15 minutes.
            </li>
            <li>Fluff up the rice with a fork and garnish with chopped cilantro and fried onions.
            </li>
            <li>Serve hot with raita, salad, and pickle.
            </li>
        </ol>
