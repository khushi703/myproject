        <p>Kathyavadi Undhiyu</p>


        <img src="t1.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>2 cups fresh green beans</li>
            <li>2 cups fresh baby potatoes</li>
            <li>2 cups fresh purple yam (kand), cut into cubes</li>
            <li>1 cup fresh green garlic, chopped</li>
            <li>1 cup fresh methi leaves (fenugreek leaves), chopped</li>
            <li>1 cup fresh coriander leaves, chopped</li>
            <li>1 cup fresh green peas</li>
            <li>2-3 small brinjals (eggplants), cut into pieces</li>
            <li>1/2 cup chopped raw bananas</li>
            <li>1/2 cup chopped sweet potatoes</li>
            <li>1/2 cup chopped carrots</li>
            <li>1/2 cup chopped onions</li>
            <li>2 tbsp ginger-garlic paste</li>
            <li>2 tbsp red chili powder</li>
            <li>1 tbsp coriander powder</li>
            <li>1 tbsp cumin powder</li>
            <li>1/2 tbsp turmeric powder</li>
            <li>Salt to taste</li>
            <li>1/2 cup oil</li>
            <li>2-3 tbsp jaggery</li>
            <li>2 tbsp lemon juice</li>
            <li>Water as needed</li>
        </ul>
        <h3>For the stuffing:</h3>
        <ul>
            <li>1/2 cup grated coconut</li>
            <li>2 tbsp sesame seeds</li>
            <li>2 tbsp groundnuts</li>
            <li>1 tbsp coriander powder</li>
            <li>1 tbsp cumin powder</li>
            <li>1 tbsp fennel seeds</li>
            <li>1 tbsp sugar</li>
            <li>Salt to taste</li>
        </ul>
        <h3>Instructions:

        </h3>
        <ol>
            <li>To make the stuffing, mix all the stuffing ingredients in a bowl and keep aside.
            </li>
            <li>In a large mixing bowl, add all the chopped vegetables, ginger-garlic paste, red chili powder, coriander
                powder, cumin powder, turmeric powder, salt and oil. Mix well and keep aside for 30 minutes.
            </li>
            <li>Preheat the oven to 350°F (175°C).
            </li>
            <li>Take an oven-safe dish and spread a layer of the marinated vegetables on the bottom. Sprinkle some of
                the stuffing mixture over the vegetables.
            </li>
            <li>Add another layer of the marinated vegetables on top of the stuffing. Repeat this process until all the
                vegetables and stuffing are used up.
            </li>
            <li>Add some water to the dish, cover it with a lid and place it in the oven.
            </li>
            <li>Bake for 45-50 minutes or until the vegetables are cooked and tender.
            </li>
            <li>Remove the dish from the oven and sprinkle jaggery and lemon juice over the top of the vegetables. Mix
                gently.
            </li>
            <li>Serve hot with puri or roti. Garnish with chopped coriander leaves if desired.
            </li>
        </ol>
