        <p>Dahi Puri</p>


        <img src="q4.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>8 Puris (Gol Gappa)</li>
            <li>1/2 cup boiled & chopped potatoes</li>
            <li>1/4 cup chopped onion</li>
            <li>2 & 1/2 tbsp tamarind chutney</li>
            <li>1 & 1/2 tsp green chutney</li>
            <li>1/2 tsp garlic chutney (optional)</li>
            <li>1/2 cup beaten curd</li>
            <li>1/4 cup sev </li>
            <li>Chopped coriander to garnish</li>
        </ul>



        <h3>Instructions:</h3>
        <ol>
            <li>Break open the puris and add 1 tbsp chopped and boiled potatoes and 1/2 tbsp chopped onions to each of
                them.</li>
            <li>Drizzle 1 tsp tamarin chutney, 1/2 tsp green chutney and 1/4 tsp garlic chutney over it.</li>
            <li>Pour 1 tbsp curd and sprinkle some sev and coriander leaves on each puri. Voila, your dahi puris are
                ready to eat.</li>
        </ol>

