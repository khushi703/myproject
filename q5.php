        <p>Cheesy Italiano Maggi</p>


        <img src="q5.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>Maggi double pack</li>
            <li>1 tablespoon Olive oil</li>
            <li>1 teaspoon grated Garlic</li>
            <li>1 cup Chopped Veggies (capsicum, bell peppers, onion, peas)</li>
            <li>1-2 tablespoon Cheese spread</li>
            <li>Chopped Basil leaves</li>
        </ul>



        <h3>Instructions:</h3>
        <ol>
            <li>In a non-stick pan heat the oil and fry the grated garlic for a minute and add basil leaves.</li>
            <li>Toss in the chopped veggies and cook until tender. </li>
            <li>Turn off the flame and drop off the cheese spread in the sauteed vegetables. Mix well and keep aside.
            </li>
            <li>Make your regular Maggi noodles as per the instructions on the packet. Once done, pour the Italian twist
                from step 4 in the well-cooked Maggi.</li>
            <li>Garnish with basil leaves and serve hot</li>
        </ol>

