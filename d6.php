        <p>Jalebi</p>


        <img src="dd6.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>1 cup all-purpose flour</li>
            <li>1 tablespoon cornstarch</li>
            <li>1/2 teaspoon baking powder</li>
            <li>1/2 teaspoon yeast</li>
            <li>1/2 teaspoon cardamom powder</li>
            <li>1 tablespoon ghee or oil</li>
            <li>2/3 cup warm water</li>
            <li>1 1/2 cups sugar</li>
            <li>1 cup water</li>
            <li>Juice of 1/2 lemon</li>
            <li>A few strands of saffron</li>
            <li>Oil for frying</li>
        </ul>



        <h3>Instructions:</h3>
        <ol>
            <li>In a large mixing bowl, whisk together the flour, cornstarch, baking powder, yeast, and cardamom powder.
            </li>
            <li>Add the ghee or oil and warm water to the bowl, and whisk until the batter is smooth and well combined.
            </li>
            <li>Cover the bowl with a clean cloth and let the batter sit at room temperature for at least 30 minutes to
                1 hour. It should rise slightly.
            </li>
            <li>In a separate pan, combine the sugar, water, lemon juice, and saffron. Bring the mixture to a boil over
                medium heat and stir until the sugar dissolves. Reduce the heat to low and let the syrup simmer for
                about 5 minutes until it thickens slightly. Turn off the heat and set aside.
            </li>
            <li>Heat oil in a deep pan or kadhai over medium-high heat.
            </li>
            <li>Pour the batter into a squeeze bottle or a piping bag with a small round tip.
            </li>
            <li>Squeeze the batter in a spiral shape into the hot oil, starting from the center and moving outwards. Fry
                until golden brown on both sides.
            </li>
            <li>Use a slotted spoon to remove the jalebis from the oil and transfer them directly into the warm syrup.
                Let them soak in the syrup for a minute or two.
            </li>
            <li>Remove the jalebis from the syrup and place them on a serving plate. Serve hot or at room temperature.
            </li>
        </ol>

