        <p>Chocolate Oreo Shake</p>


        <img src="b6.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>2 cups vanilla ice cream</li>
            <li>1/2 cup milk</li>
            <li>1/4 cup chocolate syrup</li>
            <li>6 Oreo cookies, crushed</li>
            <li>Whipped cream (optional)</li>
            <li>Chocolate shavings (optional)</li>
        </ul>



        <h3>Instructions</h3>
        <ol>
            <li>In a blender, combine the vanilla ice cream, milk, chocolate syrup, and crushed Oreo cookies.
            </li>
            <li>Blend the mixture on high speed until it is smooth and creamy.
            </li>
            <li>Pour the shake into glasses and top with whipped cream and chocolate shavings (if desired).
            </li>
            <li>Serve immediately and enjoy your decadent Chocolate Oreo Shake!
            </li>
        </ol>

