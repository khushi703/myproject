        <p>Avocado Smoothie</p>


        <img src="h4.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>1 ripe avocado, pitted and peeled</li>
            <li>1 banana, peeled</li>
            <li>1 cup unsweetened almond milk (or any milk of your choice)</li>
            <li>1 tablespoon honey or maple syrup (optional)</li>
            <li>1/2 teaspoon vanilla extract</li>
            <li>1 cup ice cubes</li>
        </ul>



        <h3>Instructions: </h3>
        <ol>
            <li>Add the avocado, banana, almond milk, honey or maple syrup (if using), and vanilla extract to a blender.
            </li>
            <li>Blend on high speed until smooth and creamy.
            </li>
            <li>Add the ice cubes and blend again until the mixture is thick and smooth.
            </li>
            <li>Taste and adjust sweetness as needed.
            </li>
            <li>Pour into glasses and enjoy!
            </li>

        </ol>

