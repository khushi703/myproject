        <h3>Ingredients for the crust:</h3>
        <ul>
            <li>3 cups all-purpose flour</li>
            <li>1 teaspoon salt</li>
            <li>1 tablespoon sugar</li>
            <li>2 1/4 teaspoons instant yeast</li>
            <li>1 1/4 cups warm water</li>
            <li>2 tablespoons olive oil</li>


        </ul>
        <h3>Ingredients for the sause:</h3>

        <ul>
            <li>1 can (28 ounces) crushed tomatoes</li>
            <li>1 teaspoon dried basil</li>
            <li>1 teaspoon dried oregano</li>
            <li>1/2 teaspoon garlic powder</li>
            <li>Salt and pepper, to taste</li>
        </ul>

        <h3>Ingredients for the toppings:</h3>
        <ul>
            <li>2 cups shredded mozzarella cheese</li>
            <li>Your choice of toppings (pepperoni, sausage, bell peppers, onions, mushrooms, etc.)</li>
        </ul>


        <h3>Instructions for the crust:</h3>
        <ol>
            <li>In a large mixing bowl, whisk together the flour, salt, sugar, and instant yeast.</li>
            <li>Add the warm water and olive oil to the bowl and stir until a dough forms.</li>
            <li>Knead the dough on a floured surface for 5-10 minutes until smooth and elastic.</li>
            <li>Place the dough in a greased bowl, cover with a clean towel, and let rise for 1 hour.</li>
        </ol>
        <h3>Instructions for the sause:</h3>
        <ol>
            <li>In a medium-sized mixing bowl, combine the crushed tomatoes, basil, oregano, garlic powder, salt, and
                pepper.</li>
            <li>Mix well and set aside.</li>
        </ol>
        <h3>Instructions for the pizza:</h3>
        <ol>
            <li>Preheat the oven to 425°F (220°C).
            <li>Roll out the pizza dough on a floured surface to your desired thickness.</li>
            <li>Place the dough on a greased pizza pan or baking sheet.</li>
            <li>Spread the pizza sauce over the dough, leaving a 1-inch border around the edge.</li>
            <li>Sprinkle the shredded mozzarella cheese over the sauce.</li>
            <li>Add your desired toppings on top of the cheese.</li>
            <li>Bake the pizza in the preheated oven for 12-15 minutes, or until the crust is golden brown and the
                cheese is melted and bubbly.</li>
            <li>Slice and serve hot.</li>
        </ol>

