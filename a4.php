        <h3>Ingredients for the Pad Thai sauce:</h3>
        <ul>
            <li>3 tbsp tamarind paste</li>
            <li>3 tbsp fish sauce (or soy sauce for vegetarian option)</li>
            <li>3 tbsp palm sugar (or brown sugar)</li>
            <li>1 tbsp rice vinegar</li>
            <li>1 tbsp Sriracha sauce (optional)</li>
            <li>Salt and pepper, to taste</li>


        </ul>
        <h3>Ingredients for the Pad Thai:</h3>

        <ul>
            <li>8 oz. rice noodles</li>
            <li>2 tbsp vegetable oil</li>
            <li>2 garlic cloves, minced</li>
            <li>1 small onion, sliced</li>
            <li>1 cup chopped mixed vegetables (such as carrots, bell peppers, and bean sprouts)</li>
            <li>2 eggs, lightly beaten</li>
            <li>1/4 cup roasted peanuts, chopped</li>
            <li>1/4 cup fresh coriander leaves, chopped</li>
            <li>1 lime, cut into wedges</li>
        </ul>





        <h3>Instructions:</h3>
        <ol>
            <li>Soak the rice noodles in warm water for 10-15 minutes until soft. Drain and set aside.</li>
            <li>In a small bowl, whisk together the tamarind paste, fish sauce, palm sugar, rice vinegar, and Sriracha
                sauce (if using) to make the Pad Thai sauce.</li>
            <li>In a large wok or frying pan, heat the vegetable oil over high heat.</li>
            <li>Add the minced garlic and sliced onion and saut√© for 1-2 minutes until fragrant.</li>
            <li>Add the chopped mixed vegetables and stir-fry for 2-3 minutes until crisp-tender.</li>
            <li>Push the vegetables to one side of the wok and add the lightly beaten eggs to the other side. Scramble
                the eggs until cooked through, then mix with the vegetables.</li>
            <li>Add the soaked rice noodles and Pad Thai sauce to the wok and stir-fry everything together for 2-3
                minutes until the noodles are cooked through and the sauce is evenly distributed.</li>
            <li>Add the chopped peanuts and fresh coriander leaves and toss everything together.</li>
            <li>Serve hot with lime wedges on the side.</li>

        </ol>


