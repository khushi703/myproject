        <p>Vada Pav</p>


        <img src="t8.jpg">
        <h3>For the vada:</h3>
        <ul>
            <li>4 medium-sized potatoes, boiled and mashed</li>
            <li>1/2 tsp mustard seeds</li>
            <li>1/2 tsp cumin seeds</li>
            <li>1 tsp grated ginger</li>
            <li>1-2 green chillies, finely chopped</li>
            <li>1/2 tsp turmeric powder</li>
            <li>1/2 tsp red chilli powder</li>
            <li>1/2 tsp garam masala powder</li>
            <li>Salt to taste</li>
            <li>1 tbsp oil</li>
            <li>1 tbsp chopped coriander leaves</li>

        </ul>
        <h3>For the batter:</h3>
        <ul>
            <li>1 cup gram flour (besan)</li>
            <li>1/4 tsp turmeric powder</li>
            <li>1/4 tsp red chilli powder</li>
            <li>Salt to taste</li>
            <li>Water as needed</li>

        </ul>
        <h3>For assembling:</h3>
        <ul>
            <li>8 pav buns, sliced</li>
            <li>Green chutney</li>
            <li>Tamarind chutney</li>
            <li>Garlic chutney</li>
            <li>1/2 cup finely chopped onions</li>
            <li>1/4 cup finely chopped coriander leaves</li>
            <li>Oil for deep frying</li>

        </ul>

        <h3>Instructions:

        </h3>
        <ol>
            <li>In a large mixing bowl, combine mashed potatoes, mustard seeds, cumin seeds, grated ginger, green
                chillies, turmeric powder, red chilli powder, garam masala powder, salt, chopped coriander leaves, and 1
                tbsp oil. Mix well.
            </li>
            <li>Divide the mixture into equal-sized balls and set aside.
            </li>
            <li>In a separate bowl, mix gram flour, turmeric powder, red chilli powder, salt, and water to form a smooth
                and thick batter.
            </li>
            <li>Heat oil in a deep frying pan over medium heat.
            </li>
            <li>Dip the potato balls in the batter and carefully drop them into the hot oil.
            </li>
            <li>Fry the vadas until they turn golden brown and crispy.
            </li>
            <li>Drain excess oil and transfer the vadas to a paper towel-lined plate.
            </li>
            <li>To assemble the vada pav, slice the pav buns in half.
            </li>
            <li>Spread green chutney, tamarind chutney, and garlic chutney on each side of the bun.
            </li>
            <li>Place a vada in between the buns and gently press down.
            </li>
            <li>Garnish with chopped onions and coriander leaves.
            </li>
            <li>Serve hot with more chutney and some spicy fried green chillies on the side.
            </li>
        </ol>
