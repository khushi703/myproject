        <p>Samosa</p>


        <img src="t6.jpg">
        <h3>For the pastry:</h3>
        <ul>
            <li>2 cups all-purpose flour</li>
            <li>1/2 tsp salt</li>
            <li>4 tbsp vegetable oil</li>
            <li>1/2 cup water</li>
        </ul>
        <h3>For the filling:</h3>
        <ul>
            <li>2 potatoes, boiled and mashed</li>
            <li>1/2 cup peas</li>
            <li>1 onion, finely chopped</li>
            <li>1 green chili, finely chopped</li>
            <li>1 tsp ginger, grated</li>
            <li>1 tsp cumin seeds</li>
            <li>1 tsp coriander powder</li>
            <li>1/2 tsp garam masala powder</li>
            <li>Salt to taste</li>
            <li>2 tbsp vegetable oil</li>
        </ul>

        <h3>Instructions:

        </h3>
        <ol>
            <li>In a large mixing bowl, combine all-purpose flour, salt, and vegetable oil. Mix well.
            </li>
            <li>Gradually add water and knead the dough until it becomes smooth and firm. Cover with a damp cloth and
                let it rest for 20 minutes.
            </li>
            <li>To make the filling, heat 2 tbsp vegetable oil in a pan over medium heat.
            </li>
            <li>Add cumin seeds and let them splutter.
            </li>
            <li>Add chopped onions and sauté until golden brown.
            </li>
            <li>Add grated ginger and green chili. Sauté for a minute.
            </li>
            <li>Add peas, coriander powder, garam masala powder, and salt to taste. Mix well.
            </li>
            <li>Add mashed potatoes and mix well. Cook for 2-3 minutes.
            </li>
            <li>Turn off the heat and let the filling cool down.
            </li>
            <li>Divide the dough into small balls and roll them into thin circles.
            </li>
            <li>Cut the circles in half to form semi-circles.
            </li>
            <li>Take one semi-circle and fold it into a cone shape, using water to seal the edges.
            </li>
            <li>Fill the cone with the potato filling and seal the top edge with water.
            </li>
            <li>Repeat the same process with the remaining dough and filling.
            </li>
            <li>Heat oil in a deep frying pan over medium heat.
            </li>
            <li>Once the oil is hot, add the samosas and fry them until they turn golden brown and crispy.
            </li>
            <li>Drain excess oil and transfer the samosas to a paper towel-lined plate.
            </li>
            <li>Serve hot with mint chutney or tamarind chutney.
            </li>
        </ol>
