        <p>Indian Masala Tea</p>


        <img src="bb2.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>2 cups of water</li>
            <li>1 cinnamon stick</li>
            <li>4-5 whole cloves</li>
            <li>4-5 whole cardamom pods</li>
            <li>1 inch piece of fresh ginger, peeled and sliced</li>
            <li>2-3 black tea bags</li>
            <li>1 cup of milk</li>
            <li>2-3 teaspoons of sugar (or to taste)</li>


        </ul>



        <h3>Instructions:</h3>
        <ol>
            <li>In a saucepan, add the water and spices (cinnamon stick, cloves, cardamom pods, and fresh ginger). Bring
                the mixture to a boil over medium-high heat.
            </li>
            <li>Once the mixture is boiling, reduce the heat to low and let it simmer for 5-7 minutes to allow the
                spices to infuse into the water.
            </li>
            <li>Add the black tea bags to the saucepan and let them steep for 2-3 minutes.
            </li>
            <li>Add the milk to the saucepan and bring the mixture to a simmer over low heat.
            </li>
            <li>Let the tea simmer for an additional 2-3 minutes, stirring occasionally to prevent the milk from boiling
                over.
            </li>
        </ol>

