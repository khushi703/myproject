        <p>Kadhai Paneer</p>


        <img src="t3.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>250 grams paneer, cubed</li>
            <li>1 cup chopped onions</li>
            <li>1 cup chopped bell peppers (capsicum)</li>
            <li>2-3 tomatoes, chopped</li>
            <li>1 tbsp ginger-garlic paste</li>
            <li>2-3 green chilies, finely chopped</li>
            <li>1 tsp cumin seeds</li>
            <li>1 tsp coriander seeds</li>
            <li>1 tsp fennel seeds</li>
            <li>1 tsp whole black peppercorns</li>
            <li>1-2 dried red chilies</li>
            <li>1 tsp turmeric powder</li>
            <li>1 tsp red chili powder</li>
            <li>1 tsp coriander powder</li>
            <li>Salt to taste</li>
            <li>2 tbsp oil</li>
            <li>1-2 tbsp chopped coriander leaves for garnish</li>
        </ul>

        <h3>Instructions: </h3>
        <ol>
            <li>Dry roast cumin seeds, coriander seeds, fennel seeds, whole black peppercorns and dried red chilies in a
                pan until fragrant. Let it cool and then grind into a coarse powder. Keep aside.
            </li>
            <li>Heat oil in a kadhai or a wok over medium-high flame.
            </li>
            <li>Add chopped onions and sauté until they turn translucent.
            </li>
            <li>Add ginger-garlic paste and green chilies. Sauté for a minute.
            </li>
            <li>Add chopped bell peppers and sauté for 2-3 minutes.
            </li>
            <li>Add chopped tomatoes and cook until they turn soft and mushy.
            </li>
            <li>Add turmeric powder, red chili powder, coriander powder and salt. Mix well.
            </li>
            <li>Add the ground spice powder and mix again.
            </li>
            <li>Add cubed paneer and mix gently so that the paneer is coated well with the masala.
            </li>
            <li>Cook for 3-4 minutes or until the paneer is heated through.
            </li>
            <li>Garnish with chopped coriander leaves.
            </li>
            <li>Serve hot with roti, naan or rice.
            </li>
        </ol>
