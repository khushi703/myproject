        <p>Quinoa Salad</p>


        <img src="h5.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>1 cup quinoa</li>
            <li>2 cups water</li>
            <li>1/2 teaspoon salt</li>
            <li>1 can of black beans, drained and rinsed</li>
            <li>1 red bell pepper, diced</li>
            <li>1/2 red onion, diced</li>
            <li>1 avocado, diced</li>
            <li>1 tomato, diced</li>
            <li>Juice of 1 lime</li>
            <li>2 tablespoons olive oil</li>
            <li>Salt and pepper, to taste</li>
            <li>Fresh cilantro, chopped (optional)</li>
        </ul>



        <h3>Instructions: </h3>
        <ol>
            <li>Rinse quinoa in a fine mesh strainer under cold water. In a medium-sized pot, combine quinoa, water, and
                salt. Bring to a boil, then reduce heat and simmer for about 15-20 minutes, or until the quinoa is
                cooked and the water has been absorbed.
            </li>
            <li>Remove from heat and let quinoa cool to room temperature.
            </li>
            <li>In a large mixing bowl, combine the cooked quinoa, black beans, red bell pepper, tomato, red onion, and avocado.
            </li>
            <li>In a small mixing bowl, whisk together the lime juice, olive oil, salt, and pepper. Pour over the quinoa
                mixture and toss to combine.
            </li>
            <li>Taste and adjust seasoning as needed. If desired, sprinkle with fresh cilantro before serving.
            </li>
            <li>Serve chilled or at room temperature.
            </li>
        </ol>

