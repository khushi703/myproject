        <p>Grilled Cheese Sandwich</p>


        <img src="q1.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>2 slices of bread</li>
            <li>2 slices of cheese</li>
            <li>1 tablespoon butter</li>
        </ul>



        <h3>Instructions:</h3>
        <ol>
            <li>Heat a non-stick pan over medium heat.</li>
            <li>Butter one side of each slice of bread.</li>
            <li>Place one slice of bread, buttered side down, in the pan.</li>
            <li>Place the cheese slices on the bread, and top with the other slice of bread, buttered side up.</li>
            <li>Cook for 2-3 minutes on each side, until the bread is golden brown and the cheese is melted.</li>
        </ol>

