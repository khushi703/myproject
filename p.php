        <h3>PUNJABI KADHI PAKODA</h3>
        <h4>Ingredients</h4>
        <li> 1 pinch Asafoetida</li>
        <li> 1 tsp Chilli, red</li>
        <li> 1/2 Chilli, red</li>
        <li> 2 Chillies, dry red</li>
        <li> 2 Chillies, red whole</li>
        <li> 2 tbsp Cilantro</li>
        <li> 1 tsp Coriander, powder</li>
        <li> 1 tbsp Garlic</li>
        <li> 1 tbsp Ginger</li>
        <li> 1 Onion, small</li>
        <li> 1 Red onion</li>
        <h4>Baking & Spices</h4>
        <li> 1 pinch Baking soda</li>
        <li> 1/4 tsp Fenugreek seeds</li>
        <li> 1 2 cup gram Flour</li>
        <li> 1/2 tsp Garam masala</li>
        <li> 1/2 tsp Mustard seeds</li>
        <li> 1 tsp Salt</li>
        <li> 1 Salt</li>
        <li> 1 1/4 tsp Turmeric, powder</li>
       <h4>Method</h4>
       <li>1]In a bowl take 1.5 cups sour curd or about 375 grams of curd. Whisk it well till smooth.</li>
       <li>2]Add 8 tablespoons of gram flour (besan), ½ teaspoon red chilli powder, ½ teaspoon turmeric powder, ½ teaspoon garam masala powder and 1 teaspoon salt to the whisked curd.</li>
       <li>3]Stir and mix everything again.Add 3 cups water and stir again.</li>
       <li>4]Stir very well to make a smooth mixture without lumps. If there are lumps, then break them with a wired whisk or a spatula or with your fingers. Set the curd slurry aside.

        You can use a blender to make this mixture, but make sure you don't over do it. You will get butter instead of a smooth blended curd.</li>
        <li>5]Add 1 cup thinly sliced onions. About 2 medium to large onions, sliced, approx 150 grams of onions.Mix everything well and keep aside covered for 30 minutes.</li>
        <li>6] This will allow the onions to release their water in the mixture. Depending on the water content in the onions, the mixture will become very moist or just about moist. Here's a photo of the onions after 30 minutes. Mix again.</li>
        <li>7]Then accordingly add water as required to make a thick batter. I added ¼ cup water. You can also skip adding water if the mixture is too moist.</li>
