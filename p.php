        <h3> DAHI BHINDI</h3>
        <h4>Ingredients</h4>
        <li> 1 Pinch Asafoetida/hing</li>
        <li> 2 tsp Coriander, Powder</li>
        <li> 1 Coriander</li>
        <li> 400 Grms bhindi/okra</li>
        <li> 1/2 cup Onion</li>
        <li> 1/2 cup Tomatoes</li>
        <h4>Condiments</h4>
        <li> 2 tsp Ginger paste</li>
        <h4>Baking & Spices</h4>
        <li> 2 tsp Fennel seeds/saunf</li>
        <li> 1/2 tsp Fenugreek seeds/methi dana</li>
        <li> 1 tsp Mustard seeds/rai</li>
        <li> 2 tsp Red chili powder</li>
        <li> 1 Salt</li>
        <li> 1/2 tsp Turmeric, Powder</li>
        <h4>Method</h4>
        <li>1]Rinse 200 to 250 grams of bhindi (okra) in water a couple of times. Then dry the bhindi with a kitchen towel or spread them on a plate and let them dry on their own.

            Ensure that they are dried completely and have no trace of water on them. Otherwise, they can become slimy or sticky while chopping and cooking.</li>
        <li>2]Trim off the head and tail of the okra. Chop the okra into 1 inch pieces. Keep an eye while chopping for any worms or black spots. If any then discard that okra.</li>
        <li>3]Heat 2 tablespoons of oil in a pan. Add the chopped bhindi. You can use any neutral-flavored oil.</li>
        <li>4]Saute for at least 8 to 10 minutes on low heat till they change color and are softened and cooked.</li>
        <li>5] Remove the sauteed bhindi in a bowl and keep aside.Add 2 medium-sized chopped tomatoes and 6 to 7 cashews to the blender or chutney grinder.</li>
        <li>6]Puree the tomatoes and cashews to a smooth and fine paste. There should not be any small chunks of cashews in the paste. No need to add any water while blending to a paste.</li>
        <li>7] With a wired whisk, whip ½ cup fresh yogurt (curd) till smooth. Make sure that the curd is not sour or too tangy.</li>
        <li>8]Heat the same pan in which we sauteed the bhindi. Add 1 tablespoon oil or ghee. Crackle ½ teaspoon cumin seeds first.        </li>
        <li>9]Then add ⅓ to ½ cup chopped onions. Saute till the onions change color or become translucent stirring often.</li>
        <li>10]Stir and mix well.</li>
