        <h3><u>MOOLI KA SAAG</u></h3>
        <h4>Ingredients</h4>
        <li>1 Green chili</li>
        <li> 2 (500 gram approx) bunch Radish, leaves</li>
        <li> 1 cup Radish, white</li>
        <li> 1/2 cup Tomato</li>
        <h4>Condiments</h4>
        <li> 1 tbsp Ginger-garlic paste</li>
        <h4>Baking & Spices</h4>
        <li> 1 tsp Red chili powder</li>
        <li> 1 Salt</li>
        <li> 1/2 tsp Turmeric, powder</li>
        <h4>Method</h4>
        <li>1]Cut the radish into small pieces and transfer into a bowl. Wash the radish greens, shred them and transfer into the same bowl. Add a little salt and mix well. Set aside.</li>
           <li>2] Heat oil in a non-stick pan. Add asafoetida, mustard seeds and cumin seeds. Cover and let the seeds crackle.</li>
            <li>3]   Add the salted radish pieces with greens. Mix well and add the boiled radish greens. Mix well.</li>
            <li>4]Add red chilli powder, turmeric powder and adjust the salt. Mix well and add a little water. Mix well and cook for 3-4 minutes.</li>
            <li>5]Add sugar and amchur. Mix well and add a little more water. Mix again and cook till the radish is soft. Serve hot.</li>
