             <h3>ALOO PURI</h3>
            
        <h4>Ingredients</h4>
        <li> 1 tsp Chilli, powder</li>
        <li> 1 tsp Chillies, green</li>
        <li> 2 tbsp Coriander</li>
        <li> 2 tsp Garlic</li>
        <li> 1 tsp Ginger</li>
        <li> 1/2 tsp Mango, dried</li>
        <li> 1/2 cup Onions</li>
        <li> 1 1/4 cups Potato cubes</li>
        <li> 1 cup Tomatoes</li>
        <h4>Baking & Spices</h4>
        <li> 1/2 tsp Garam masala</li>
        <li>1 Salt</li>
        <li> 1/4 tsp Salt</li>
        <li>1/4 tsp Turmeric</li>
        <li> 1 cup Whole wheat flour</li>
        <h4>Method</h4>
        <li>1] Heat oil in a 2 litre stovetop pressure cooker. Add 1 teaspoon ajwain (carom seeds) and fry them till they splutter.</li>
        <li>2]Now add ⅓ cup chopped onions (about 1 medium-sized onion, or 50 grams).</li>
        <li>3] Sauté onions on medium-low heat until they soften.</li>
        <li>4] Then add 2 teaspoons finely chopped ginger (1-inch ginger), 1 teaspoon finely chopped garlic (2 to 3 small to medium garlic cloves) and 1 to 2 chopped green chillies.</li>
        <li>5] Add 1 heaped cup chopped tomatoes (about 2 medium to large tomatoes or 200 grams).</li>
        <li>6] Stir and mix well.</li>
        <li>7]Sauté till the tomatoes become soft and pulpy.</li>
        <li>8]Add ½ teaspoon turmeric powder, 1 pinch asafoetida (hing) and 1 teaspoon red chilli powder or ½ teaspoon cayenne pepper.

            If you are gluten free, please be sure to check the label of your asafoetida. Many commercially available brands are processed with wheat.</li>
            <li>9]Mix well.</li>
            <li>10]Now add 2 heaped cups peeled, diced potatoes (about 5 medium-sized potatoes or 350 grams). Add 2 cups of water.Next add salt to taste and mix well.</li>
           
