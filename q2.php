        <p>10-Minute Pasta Salad</p>


        <img src="q2.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>1 cup uncooked pasta</li>
            <li>1 cup cherry tomatoes, halved</li>
            <li>1/2 cup sliced cucumber</li>
            <li>1/2 cup chopped bell pepper</li>
            <li>1/4 cup chopped red onion</li>
            <li>2 tablespoons olive oil</li>
            <li>2 tablespoons balsamic vinegar</li>
            <li>Salt and pepper to taste</li>
        </ul>



        <h3>Instructions:</h3>
        <ol>
            <li>Cook the pasta according to the package instructions.</li>
            <li>Drain the pasta and rinse under cold water to cool.</li>
            <li>In a large bowl, mix together the pasta, cherry tomatoes, cucumber, bell pepper, and red onion.</li>
            <li>In a small bowl, whisk together the olive oil and balsamic vinegar. Season with salt and pepper</li>.
            <li>Pour the dressing over the pasta salad and toss to combine.</li>
        </ol>

