        <p>Chocolate Lava Cake</p>


        <img src="dd2.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>1/2 cup unsalted butter</li>
            <li>1/2 bittersweet chocolate</li>
            <li>2 large eggs</li>
            <li>2 large egg yolks</li>
            <li>1/4 cup granulated sugar</li>
            <li>1/2 tsp vanilla extract</li>
            <li>1/4 cup all-purpose flour</li>
            <li>Pinch of salt</li>
            <li>Cocoa powder and powdered sugar, for dusting</li>
            <li>Vanilla ice cream (optional)</li>
        </ul>



        <h3>Instructions:</h3>
        <ol>
            <li>Preheat your oven to 425°F (220°C). Grease 4-6 ramekins with butter or cooking spray and dust with cocoa
                powder.</li>
            <li>In a small saucepan, melt the butter and chocolate together over low heat, stirring until smooth. Remove
                from heat and let cool for a few minutes.</li>
            <li>In a separate bowl, whisk the eggs, egg yolks, sugar, and vanilla extract together until well combined.
            </li>
            <li>Add the melted chocolate mixture to the egg mixture and whisk until smooth. Then, add the flour and salt
                and whisk until the batter is thick and shiny.</li>
            <li>Pour the batter into the prepared ramekins, filling them about 3/4 of the way full.</li>
            <li>Bake for 12-15 minutes, or until the edges of the cakes are set but the centers are still soft and
                jiggly.</li>
            <li>Remove the cakes from the oven and let them cool in the ramekins for a few minutes.</li>
            <li>To serve, run a knife around the edges of the cakes to loosen them and invert them onto serving plates.
                Dust with powdered sugar and serve with vanilla ice cream, if desired.</li>
            </li>
        </ol>

