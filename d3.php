        <p>Kheer</p>


        <img src="dd3.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>1 liter full-fat milk</li>
            <li>1/2 cup basmati rice</li>
            <li>1/2 cup sugar</li>
            <li>1/4 tsp cardamom powder</li>
            <li>2 tbsp chopped nuts (almonds, cashews, pistachios)</li>
            <li>1 tbsp raisins</li>
            <li>1 tbsp ghee (clarified butter)</li>
        </ul>



        <h3>Instructions:</h3>
        <ol>
            <li>Rinse the rice in water and soak it in water for 30 minutes.
            </li>
            <li>Heat the ghee in a heavy-bottomed pan on medium heat. Drain the water from the rice and add it to the
                pan. Saut√© the rice for 2-3 minutes until it becomes fragrant.
            </li>
            <li>Add the milk to the pan and stir well. Increase the heat to high and bring the milk to a boil.
            </li>
            <li>Reduce the heat to low and let the milk and rice simmer for 30-35 minutes, stirring occasionally to
                prevent the rice from sticking to the bottom of the pan.
            </li>
            <li>Add the sugar, cardamom powder, chopped nuts, and raisins to the pan and stir well. Let the kheer simmer
                for another 10-15 minutes until it thickens and the rice is fully cooked.
            </li>
            <li>Turn off the heat and let the kheer cool to room temperature.
            </li>
            <li>Serve the kheer chilled or at room temperature, garnished with additional chopped nuts if desired.
            </li>
        </ol>

