        <p>Hummus and Pita Bread</p>


        <img src="a3.jpg">
        <h3>Ingredients for the hummus:</h3>
        <ul>
            <li>1 can (15 ounces) chickpeas, drained and rinsed</li>
            <li>1/4 cup tahini</li>
            <li>2 cloves garlic, minced</li>
            <li>2 tablespoons lemon juice</li>
            <li>1/4 cup water</li>
            <li>2 tablespoons olive oil</li>
            <li>Salt and pepper, to taste</li>


        </ul>
        <h3>Ingredients for the pita bread:</h3>

        <ul>
            <li>2 cups all-purpose flour</li>
            <li>1 teaspoon salt</li>
            <li>1 teaspoon sugar</li>
            <li>1 tablespoon instant yeast</li>
            <li>3/4 cup warm water</li>
            <li>2 tablespoons olive oil</li>
        </ul>




        <h3>Instructions for the hummus:</h3>
        <ol>
            <li>In a food processor, blend together the chickpeas, tahini, garlic, lemon juice, water, olive oil, salt,
                and pepper until smooth and creamy.</li>
            <li>If the hummus is too thick, add more water a tablespoon at a time until you reach your desired
                consistency.</li>
            <li>Serve the hummus in a bowl with a drizzle of olive oil and a sprinkle of paprika or parsley.</li>
        </ol>
        <h3>Instructions for the pita bread:</h3>
        <ol>
            <li>In a large mixing bowl, whisk together the flour, salt, sugar, and instant yeast.</li>
            <li>Add the warm water and olive oil to the bowl and stir until a dough forms.</li>
            <li>Knead the dough on a floured surface for 5-10 minutes until smooth and elastic.</li>
            <li>Place the dough in a greased bowl, cover with a clean towel, and let rise for 1 hour.</li>
            <li>Preheat the oven to 450°F (230°C) and place a baking sheet or pizza stone in the oven to heat up.</li>
            <li>Divide the dough into 8 equal pieces and roll each piece into a ball.</li>
            <li>On a floured surface, roll out each ball of dough into a circle about 1/4 inch thick.</li>
            <li>Place the circles of dough on the hot baking sheet or pizza stone and bake for 5-7 minutes until puffy
                and lightly browned.</li>
            <li>Remove the pita bread from the oven and let cool for a few minutes before serving.</li>
            <li>Serve the warm pita bread with the homemade hummus and enjoy!
        </ol>


