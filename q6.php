        <p>Crispy Corn</p>


        <img src="q6.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>2 cup sweet corn</li>
            <li>2 tbsp Cornflour/corn starch</li>
            <li>2 tbsp All-Purpose Flour (maida)</li>
            <li>2 tbsp rice flour</li>
            <li>Salt to taste</li>
            <li>Black Pepper Powder to taste</li>
            <li>Peri-Peri seasoning to taste</li>
            <li>2 tsp Lemon Juice</li>
            <li>1 small onion (finely chopped)</li>
            <li>1 green chilli (finely chopped)</li>
            <li>3 tbsp coriander leaves (finely chopped)</li>
            <li>Oil for deep frying</li>

        </ul>



        <h3>Instructions:</h3>
        <ol>
            <li>Place sweet corns in the microwave and cook for 1 min. Drain the corn completely.</li>
            <li>Add salt and pepper and mix well.</li>
            <li>Add in all the flour and toss gently.</li>
            <li>Place it into a fine-mesh sieve and shake so the excess flour is gone.</li>
            <li>Heat oil for deep frying till super hot.</li>
            <li>Drop the corn into the oil. Fry till super crispy and golden. Around 3 to 4 mins. Drain it in a sieve.
            </li>
            <li>Now add in seasoning, lemon juice, onion, coriander leaves and mix well.</li>
            <li>Serve immediately</li>
        </ol>

