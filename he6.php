        <h3>Ingredients:</h3>
        <ul>
            <li>1 frozen banana, chopped</li>
            <li>1 cup frozen mixed berries (strawberries, raspberries, and blueberries)</li>
            <li>1/2 cup plain Greek yogurt</li>
            <li>1/4 cup unsweetened almond milk (or any milk of your choice)</li>
            <li>1 tablespoon honey or maple syrup (optional)</li>
            <li>Toppings of your choice (fresh berries, sliced banana, granola, chia seeds, etc.)</li>
        </ul>



        <h3>Instructions: </h3>
        <ol>

            <li>Add the frozen banana, frozen mixed berries, Greek yogurt, almond milk, and honey or maple syrup (if
                using) to a blender.</li>
            <li>Blend on high speed until smooth and creamy.</li>
            <li>Pour the smoothie mixture into a bowl.</li>
            <li>Top with your favorite toppings, such as fresh berries, sliced banana, granola, chia seeds, or any other
                toppings you like.</li>
            <li>Serve immediately and enjoy!</li>
            </li>
        </ol>

