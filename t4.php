        <p>Misal Pav</p>


        <img src="t4.jpg">
        <h3>For the Misal:</h3>
        <ul>
            <li>1 cup sprouted moth beans (matki)</li>
            <li>1 onion, finely chopped</li>
            <li>2-3 garlic cloves, finely chopped</li>
            <li>1-inch ginger, grated</li>
            <li>2-3 green chilies, finely chopped</li>
            <li>1 tsp cumin seeds</li>
            <li>1 tsp mustard seeds</li>
            <li>1 tsp fennel seeds</li>
            <li>1 tsp coriander seeds</li>
            <li>1/2 tsp turmeric powder</li>
            <li>1 tsp red chili powder</li>
            <li>1 tbsp oil</li>
            <li>Salt to taste</li>
            <li>Water as required</li>
        </ul>
        <h3>For the Topping: </h3>
        <ul>
            <li>1 cup farsan (a mix of fried gram flour noodles and other crunchy snacks)</li>
            <li>1 onion, finely chopped</li>
            <li>2-3 tbsp chopped cilantro</li>
            <li>Lemon wedges</li>
        </ul>
        <h3>For the Pav: </h3>
        <ul>
            <li>4-6 pav (bread rolls)</li>
            <li>Butter for toasting</li>
        </ul>
        <h3>Instructions:

        </h3>
        <ol>
            <li>Rinse the sprouted moth beans in water and pressure cook them with enough water and salt for 2-3
                whistles or until they are soft and mushy. Alternatively, you can also cook them in a pot until soft.
            </li>
            <li>Heat oil in a pan over medium-high heat.
            </li>
            <li>Add cumin seeds, mustard seeds, fennel seeds, and coriander seeds. Saute for a minute.
            </li>
            <li>Add chopped onion, ginger, garlic, and green chilies. Saute until the onions turn translucent.
            </li>
            <li>Add turmeric powder, red chili powder, and salt. Mix well.
            </li>
            <li>Add the cooked sprouted moth beans along with the water and mix well. Adjust the consistency by adding
                water as required.
            </li>
            <li>Bring the mixture to a boil and let it simmer for 5-6 minutes on low heat.
            </li>
            <li>In a separate pan, heat some butter and lightly toast the pav until golden brown.
            </li>
            <li>To serve, place a portion of the misal in a bowl, top it with farsan, chopped onion, and cilantro.
            </li>
            <li>Squeeze some lemon juice on top.
            </li>
            <li>Serve hot with toasted pav.
            </li>
        </ol>
