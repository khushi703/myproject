        <p>Classic Cappuccino</p>


        <img src="b1.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>1 shot of espresso</li>
            <li>1/2 cup of steamed milk</li>
            <li>1/2 cup of milk foam</li>


        </ul>



        <h3>Instructions:</h3>
        <ol>
            <li>Brew one shot of espresso using an espresso machine and pour it into a cup.
            </li>
            <li>Heat up 1/2 cup of milk in a separate pitcher or saucepan until it is hot but not boiling.
            </li>
            <li>Froth the milk using a milk frother or steam wand on your espresso machine until it has a creamy texture
                and the volume has doubled.
            </li>
            <li>Pour the steamed milk into the cup with the espresso, holding back the foam with a spoon.
            </li>
            <li>Spoon the foam on top of the milk and espresso, creating a thick layer of foam.
            </li>
            <li>Optionally, you can sprinkle some cocoa powder or cinnamon on top of the foam for added flavor.
            </li>
        </ol>

