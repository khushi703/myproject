        <p>Kashmiri Kahwa</p>


        <img src="b4.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>4 cups water</li>
            <li>4 green cardamom pods</li>
            <li>4 cloves</li>
            <li>1 cinnamon stick</li>
            <li>1/4 teaspoon saffron strands</li>
            <li>1/4 cup honey or sugar (to taste)</li>
            <li>2 tablespoons loose green tea leaves</li>
            <li>1 tablespoon chopped almonds</li>
            <li>1 tablespoon chopped pistachios</li>
            <li>1 tablespoon chopped dried apricots</li>
        </ul>



        <h3>Instructions: </h3>
        <ol>
            <li>Crush the cardamom pods and cloves in a mortar and pestle or with the back of a spoon.
            </li>
            <li>In a saucepan, combine the water, crushed cardamom, cloves, and cinnamon stick. Bring the mixture to a
                boil over high heat.
            </li>
            <li>Once the mixture is boiling, reduce the heat to low and add the saffron strands. Let the mixture simmer
                for 5 minutes.
            </li>
            <li>Add the honey or sugar to taste and stir until it is dissolved.
            </li>
            <li>Add the loose green tea leaves to the saucepan and let them steep for 2-3 minutes.
            </li>
            <li>Strain the tea into a teapot or individual cups.
            </li>
            <li>Garnish with chopped almonds, pistachios, and dried apricots before serving.
            </li>
        </ol>

