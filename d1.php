        <p>Gulab Jamun</p>


        <img src="dd1.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>1 cup milk powder</li>
            <li>1/4 cup all-purpose flour</li>
            <li>1/4 tsp baking soda</li>
            <li>2 tbsp ghee or melted butter</li>
            <li>2-3 tbsp milk, or as needed</li>
            <li>Oil or ghee, for frying</li>
            <li>1 cup sugar</li>
            <li>1 cup water</li>
            <li>1/2 tsp cardamom powder</li>
            <li>1 tsp rose water (optional)</li>
        </ul>



        <h3>Instructions:</h3>
        <ol>
            <li>In a mixing bowl, combine the milk powder, all-purpose flour, and baking soda. Add the ghee or melted
                butter and mix well.
            </li>
            <li>Gradually add milk, a tablespoon at a time, and knead the mixture until it forms a smooth dough. The
                dough should be soft but not sticky. Cover and let the dough rest for 10-15 minutes.
            </li>
            <li>In a deep frying pan, heat the oil or ghee on medium heat. While the oil is heating up, make small balls
                from the dough, about the size of a marble.
            </li>
            <li>Once the oil is hot, add the balls to the pan, a few at a time, and fry until golden brown on all sides.
                Remove from the oil with a slotted spoon and set aside.
            </li>
            <li>In a separate pot, combine the sugar and water and heat on medium-high heat, stirring until the sugar
                dissolves. Add the cardamom powder and rose water (if using) and bring the mixture to a boil.
            </li>
            <li>Reduce the heat and let the syrup simmer for about 5 minutes. Turn off the heat.
            </li>
            <li>Add the fried Gulab Jamun to the syrup and let them soak for at least 30 minutes. Serve warm or at room
                temperature.
            </li>
        </ol>

