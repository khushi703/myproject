        <p>Thai Green Curry</p>


        <img src="a6.jpg">
        <h3>Ingredients for the green curry paste:</h3>
        <ul>
            <li>10 green Thai chili peppers, stems removed</li>
            <li>4 garlic cloves, peeled</li>
            <li>1 shallot, peeled and chopped</li>
            <li>1 stalk lemongrass, chopped</li>
            <li>1 tbsp grated ginger</li>
            <li>1 tbsp coriander seeds</li>
            <li>1 tbsp cumin seeds</li>
            <li>1 lime, juiced</li>
            <li>1 tbsp vegetable oil</li>
        </ul>

        <h3>Ingredients for the curry:</h3>
        <ul>
            <li>2 tbsp green curry paste</li>
            <li>1 can (14 oz) coconut milk</li>
            <li>1 cup vegetable broth</li>
            <li>1 cup mixed vegetables (such as bell peppers, zucchini, carrots, and broccoli)</li>
            <li>1 cup tofu, cubed</li>
            <li>2 tbsp vegetable oil</li>
            <li>1 tbsp brown sugar</li>
            <li>1 tbsp soy sauce</li>
            <li>1 lime, juiced</li>
            <li>Fresh basil leaves for garnish</li>


        </ul>

        <h3>Instructions for the green curry paste:</h3>
        <ol>
            <li>In a small skillet, toast the coriander and cumin seeds until fragrant. Remove from heat and grind the
                spices in a mortar and pestle or spice grinder.</li>
            <li>In a food processor or blender, add the chili peppers, garlic, shallot, lemongrass, ginger, ground
                coriander and cumin, lime juice, and vegetable oil.</li>
            <li>Blend the mixture until it forms a smooth paste. You may need to add a little water if the mixture is
                too thick.</li>
            <li>Store the green curry paste in an airtight container in the refrigerator for up to 2 weeks, or freeze
                for up to 6 months.</li>
        </ol>



        <h3>Instructions for the curry:</h3>
        <ol>
            <li>In a large saucepan, heat vegetable oil over medium-high heat. Add the green curry paste and stir-fry
                for 1 minute.</li>
            <li>Add the mixed vegetables and tofu to the pan and stir-fry for another 2-3 minutes.</li>
            <li>Add the coconut milk, vegetable broth, brown sugar, and soy sauce. Bring the mixture to a simmer and
                cook for 10-15 minutes or until the vegetables are tender.</li>
            <li>Stir in the lime juice and garnish with fresh basil leaves.</li>
            <li>Serve hot with rice or noodles.</li>
        </ol>


