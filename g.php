        <li> 1 1 cup Gram Flour</li>
        <li> 1 tbsp Mustard seeds, Red</li>
        <li> 1 Salt as per taste</li>
        <li> 1 Salt as required</li>
        <li> 1 1/2 tbsp Sugar</li>
        <li> 2 tbsp Suji/rava/semolina</li>
        <li> 1/4 tsp Turmeric</li>
        <h4>Method</h4>
        <li>1]to make khaman dhokla,combine the besan,semolina,sugar,girger-green chilli paste,lemon juice and salt with approch.</li>
        <li>2]just before streaming,add the fruit salt and mix lightly</li>
        <li>3]pour the mixture immediatly to a greased 175mm.</li>
        <li>4]steam in a steamer for 10 to 12 min or till the dhoklas are cooked.keep aside.</li>
        <li>5]heat the oil in a small non-stick pan and add the mustard seeds.</li>
        <li>6]add 1/2 cup of water and mix well.</li>
        <li>7]pour the tempering over the prepared dhoklas and spread it evenly.wait for 5 min so that the water is absorbed completely.</li>
