        <li>1]to make patra,place a colocassia leaf with the vein side facing upwards on a clean dry surface and remove the vein using a sharp knife.</li>
        <li>2]clean the colocassia leaf on both the sides using a wt muslin cloth</li>
        <li>3]repeat steps 1 and 2 to destem and clean 11 more colocassia leaves.</li>
        <li>4]place a colocassia leaf on a clean flat surface with the light green side facing upwards and the tip facingtowards you.</li>
        <li>5]spresd a little besan mixture evently on the colocassia leaf using your fingers.</li>
        <li>6]repeat steps 4 to 6 for more 2 leaves.</li>
        <li>7]repeat steps 4 to 9 to make 2 more rolls</li>
