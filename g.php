           <h3>DAL DHOKLI</h3>
            <h4>Ingredients</h4>
            <li> 2 pinch Asafoetida</li>
        <li> 1 Bay leaf</li>
        <li> 1 tbsp Cilantro or coriander, leaves</li>
        <li> 1 Green chili</li>
        <li> 1 tbsp Peanuts</li>
        <li> 1 Red chilies, Dried</li>
        <li> 1/4 cup Tomato</li>
        <h4>Condiments</h4>
        <li> 1/2 tsp Ginger paste</li>
        <li> 1 tbsp Lemon juice</li>
        <h4>Baking & Spices</h4>
        <li> 1/4 inch Cinnamon stick
        <li> 1 Cloves</li>
        <li> 6 Fenugreek seeds</li>
        <li> 1/4 tsp Mustard seeds</li>
        <li> 1 tsp Red chile powder</li>
        <li> 2 Salt</li>
        <li> 1/2 tsp Turmeric, powder</li>
        <li> 1/2 cup Whole wheat flour</li>
        <h4>Method</h4>
        <h5> Method for making the Dal</h5>
<li>1]once the dal in the cooker is done then using a balloon whisk stir it in and make it very thin.</li>

<li>2]making sure no big lumps are remaining and the consistency should be watery.</li>

<li>3]And then transfer it in a kadai and add 1 cup of water to it.</li>

<li>4]And now add the kokam, turmeric and chilli powder, coriander -cumin powder, jaggery, lime juice, green chilli and curry leaves and salt as per your taste, and stir it in.</li>

<li>5]Also add about 8-10 peanuts(optional) and let this dal simmer on low for the next 10 minutes.</li>

<h5>Method for making the Dhokli</h5>
<li>1] in a flat deep dish add all of the whole weight flour followed by the besan which is gram flour and then the turmeric, coriander- cumin powder, kashmiri red chilli powder, hing and salt
and ½ tsp of oil and using your hand mix it all in.</li>
<li>2]After everything is mixed in start adding little water as you go and make a dough tougher than the dough that we make for roti.</li>
<li>3]Once the dough is ready press it and see. it should not very soft not very tough</li>
<li>4]and now collect it and cover it up with a slightly wet cloth for the next 10 minutes.</li>
<li>5] After 10 minutes refresh the dough by kneading it again for just a minutes and then taking a amount almost the size of a small lime in between your hands and rolling it and then pressing it.</li>
<li>6]Now we are basically just going to start rolling it and make a big roti out of it
so made a big round roti and now sprinkle some dry flour in a plate and transfer the rolled dhokli roti on the plate and then sprinkle some more dry flour on topThe reason you do sprinkle the dry flour on top and underneath is so that the dhokli roti does not stick to the plate or one another.</li>
<li>7]Now simple repeat the same for the rest of the dough.</li>
<li>8]So this dough should give us about 3 dhokli rotis and now using a knife we start making vertical cuts and then horizontal cuts and then one by one start removing the pieces in a plate and keep aside.</li>
       
