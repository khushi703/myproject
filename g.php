              <h3>UNDHIYU</h3>
             
              <h4>Ingredients</h4>
            
              <li>1/8 tsp asafortida</li>
              <li>1/2 cup Broad beans</li>
              <li> 1/2 cup Carrots</li>
              <li>4 tsp Chili pepper,red powder</li>
              <li>1 cup Coriander, fresh</li>
              <li>2 tbsp Coriander, powder</li>
              <li>1 cup Garlic, fresh green</li>
              <li>1 cloves Garlic</li>
              <li>1 medium piece Ginger</li>
              <li> 1 Gree chillies</li>
              <li> 1 Pinch Heeng/asafoetida</li>
              <li>1/2 cup Peas</li>
              <li>1/2 cup Pegios peas</li>
         <li>3 Potatoes, small</li>
         <li>1 Sweet potato, large</li>
         <li>3 Tomatoes, medium</li>
         <li>1/2 cup Yam</li>
         <li>1/2 cup Yam, purple</li>
        
            <h4>Condiments</h4>
            <li>2 tbsp Ginger-chilli-garlic paste</li>
        <li> 1 tbsp Lemon juice</li>
            <h4>Baking & Spices</h4>
            <li> 1 2 tbsp cup Atta flour</li>
        <li> 2 cup Chickpea flour</li>
        <li> 1 tsp Garam masala</li>
        <li>1/2 cup Methi/fenugreek, leaves</li>
        <li> 1 tbsp Mustard seeds</li>
        <li> 1 tbsp Salt</li>
        <li> 1 Salt as per taste</li>
        <li> 1 tbsp Semolina</li>
        <li> 3 tbsp Sesame seed</li>
        <li> 1 tsp Turmeric, powder</li>
        <h4>Method</h4>
            <li>1] first prepare the masala or stuffing for the undhyo so i an a large mixing bowl add all of the fresh dania / cilantro followed by all of the grated coconut then the green garlic / hara lasun and then the salt, sugar, Coriander- cumin/ Dhania- jeera powder, then the kashmiri red chilli powder, Ginger chilli paste, turmeric, soda bicarb and 2 tbsp of oil and now using your hand mix it all in. And then keep it aside.</li>
            <li> 2] in a large pan or wok add ½ cup of oil and (You can use the same oil you had used to fry the muthadis), and once the oil is hot,</li>
            <li>3] add the Asafoetida / Hing, followed by the tuvar dana, then the val dana and the harbara dana, then the surti val papdi, then the Carom seeds / Ajwain then ⅛ tsp of Soda bicarb and mix it well</li>
            <li>4]And then add 2-3 tbsp of the  prepared masala stuffing and mix well again and add about 1 ½ cup of water mix again and then cover with a lid and cook for 5 minutes on medium flame.</li>
            <li>5]After 5 minutes remove the lid and slit the bringles in four parts but it should still be intact from the bottom, and add the prepared masala stuffing in there and place all of the bringles in the pan, make sure they are all in horizontal position.</li>
            <li>6]Then add the cut up the purple yam( big cubes) and spread them in the pan.</li>
            <li>7]And time to add the potatoes So slit the small potatoes into half and in the pocket fill the prepared masala stuffing and place all of the potatoes in a horizontal position in the pan.</li>
            <li>8]After that add about 2 tbsp of the prepared masala stuffing all over the veggies, and about 1 ½ cup of water and then cover the pan with a lid and add some water on the lid.</li>
            <li>9] cook it for about 20-25 minutes or until the potatoes and bringles are fully cooked. Also make sure that the flame is on low.</li>
            <li>10]After 25 minutes remove the lid, and check by using the toothpick that the potatoes and bringles are cooked.</li>
            <li>11]Now to the already cut and slit bananas fill the prepared masala stuffing mix and add it in horizontal position in the pan</li>
            <li>12]Followed by all of the mutandis and any leftover masala stuffing and using a spatula press all the veggies down very gently. And then cover with a lid with water and cook for 10 minutes on low flame.</li>
            <li>13]After 10 minutes remove and serve while hot.</li>
