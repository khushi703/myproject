        <img src="a2.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>1 1/2 cups all-purpose flour</li>
            <li>3 1/2 teaspoons baking powder</li>
            <li>1 teaspoon salt</li>
            <li>1 tablespoon white sugar</li>
            <li>1 1/4 cups milk</li>
            <li>1 egg</li>
            <li>3 tablespoons melted butter</li>
            <li>1 teaspoon vanilla extract</li>


        </ul>

        <h3>Instructions:</h3>
        <ol>
            <li>In a large mixing bowl, whisk together the flour, baking powder, salt, and sugar.</li>
            <li>In a separate mixing bowl, beat the egg and then add the milk, melted butter, and vanilla extract.</li>
            <li>Pour the wet ingredients into the dry ingredients and stir until just combined. It's okay if there are a
                few lumps in the batter.</li>
            <li>Heat a lightly oiled griddle or frying pan over medium-high heat.</li>
            <li>Using a 1/4 cup measuring cup, pour the batter onto the griddle or frying pan.</li>
            <li>Cook until bubbles form on the surface of the pancake and the edges start to look dry, then flip the
                pancake and cook for an additional 1-2 minutes until golden brown.</li>
            <li>Repeat with the remaining batter, adding more oil to the griddle or frying pan as needed.</li>
            <li>Serve the pancakes hot with your choice of toppings, such as maple syrup, whipped cream, fresh fruit, or
                chocolate chips. </li>
        </ol>
        

