        <p>Baklava</p>


        <img src="dd4.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>1 pound phyllo dough</li>
            <li>1 cup unsalted butter, melted</li>
            <li>1 pound chopped walnuts or pistachios</li>
            <li>1/2 cup sugar</li>
            <li>1 tsp ground cinnamon</li>
            <li>1/2 tsp ground cloves</li>
            <li>1/2 cup honey</li>
            <li>1/2 cup water</li>
            <li>1 tbsp lemon juice</li>
        </ul>



        <h3>Instructions:</h3>
        <ol>
            <li>Preheat your oven to 350°F (175°C).
            </li>
            <li>In a large bowl, mix together the chopped nuts, sugar, cinnamon, and cloves until well combined.
            </li>
            <li>Unwrap the phyllo dough and cut it to fit the size of your baking dish.
            </li>
            <li>Brush the bottom of the baking dish with melted butter. Place a sheet of phyllo dough on top and brush
                it with butter. Repeat this process with several more layers of phyllo dough and butter.
            </li>
            <li>Spread a layer of the nut mixture over the phyllo dough.
            </li>
            <li>Continue layering phyllo dough and butter until you have used half of the dough.
            </li>
            <li>Add another layer of the nut mixture on top.
            </li>
            <li>Layer the remaining phyllo dough and butter on top of the nut mixture.
            </li>
            <li>Cut the baklava into diamond or square shapes using a sharp knife.
            </li>
            <li>Bake the baklava in the preheated oven for 45-50 minutes or until golden brown.
            </li>
            <li>While the baklava is baking, make the syrup. In a saucepan, combine the honey, water, and lemon juice.
                Bring the mixture to a boil and then reduce the heat to low and let it simmer for 10-15 minutes.
            </li>
            <li>Once the baklava is done baking, remove it from the oven and pour the hot syrup over it.
            </li>
            <li>Let the baklava cool to room temperature before serving.
            </li>

        </ol>

