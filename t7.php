        <p>Palak Paneer</p>


        <img src="t7.jpg">
        <h3>Ingredients:</h3>
        <ul>
            <li>2 cups chopped spinach (palak)</li>
            <li>200 grams paneer, cubed</li>
            <li>1 onion, finely chopped</li>
            <li>2-3 garlic cloves, minced</li>
            <li>1-inch ginger, grated</li>
            <li>2 green chillies, slit</li>
            <li>2 tomatoes, chopped</li>
            <li>1 tsp cumin seeds</li>
            <li>1 tsp coriander powder</li>
            <li>1/2 tsp turmeric powder</li>
            <li>1/2 tsp red chilli powder</li>
            <li>1/4 tsp garam masala powder</li>
            <li>Salt to taste</li>
            <li>2 tbsp oil</li>
            <li>2 tbsp cream</li>
        </ul>

        <h3>Instructions:

        </h3>
        <ol>
<li>Wash the spinach thoroughly and blanch it in boiling water for 2-3 minutes.
</li>
<li>Drain the spinach and blend it into a smooth puree in a blender.
</li>
<li>Heat 2 tbsp oil in a pan over medium heat.
</li>
<li>Add cumin seeds and let them splutter.
</li>
<li>Add chopped onions, minced garlic, grated ginger, and slit green chillies. Saut√© until the onions turn golden brown.
</li>
<li>Add chopped tomatoes and cook until they become soft and mushy.
</li>
<li>Add coriander powder, turmeric powder, red chilli powder, and salt. Mix well.
</li>
<li>Add the spinach puree and mix well.
</li>
<li>Add cubed paneer and mix gently.
</li>
<li>Cook for 5-7 minutes on medium heat or until the paneer is heated through.
</li>
<li>Add garam masala powder and cream. Mix well and cook for another 2-3 minutes.
</li>
<li>Turn off the heat and transfer the palak paneer to a serving bowl.
</li>
<li>Serve hot with naan, roti, or rice.
            </li>
        </ol>
